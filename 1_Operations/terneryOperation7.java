package org1.operations;

public class terneryOperation7 {

	public static void main(String[] args) {
		
		// Even or Odd
		// Using Ternary Operation
		// (Condition) ? if_true : if_false;p
		int number = 10;
		String result = (number % 2 == 0) ? "Even" : "Odd";
		System.out.println(result);

	}

}
