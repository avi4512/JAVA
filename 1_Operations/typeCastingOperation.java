public class typeCastingOperation {
	
	public static void main(String[] args) {
		/*
		 1. Primitive type casting
		     |
		     |
		      i. implicit (widening casting) --> smaller type to larger type    Done by compiler 
		     ii. explicit (narrow casting) -->  larger type to smaller type
		
		 2. Reference type casting (objects)
		 		|
		 		|
		 		i.upcasting(implicit)   --> subclass to superclass
		 	   ii.Downcasting(explicit) --> superclass to subclass		 
		 */
		
		int num = 10;
		float n = num;
		
	}

}
